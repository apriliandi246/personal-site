   <h1 class="title">The phenomenon of "SHOULD I LEARN"</h1>

   <p>
      Maybe you think with this blog title, "WHAT'S WRONG WITH THIS PHENOMENON".
      Yes, that's not wrong but many people do this phenomenon all the time. For
      example, if a new Javascript framework or library comes out and
      programmers in some groups or some forum say "SHOULD I LEARN THIS".
   </p>

   <p>
      If you just use that new framework or library just because it's very
      trending and a lot of people talk about it, I think why not change your
      perspective about this phenomenon. For example, why not try to visit the
      documentation and read about that framework or library. If you have time,
      find out why the author of the framework or library makes this thing, what
      problem the author solves, or even the philosophy behind this framework or
      library.
   </p>

   <p>
      For example, when I wanna learn another Javascript frontend framework, and
      that framework gives me a lot of productivity and the performance is also
      very good. Finally, I decided to choose Svelte, and Svelte solve my
      problem about productivity and performance.
   </p>

   <p>
      On the productivity side, Svelte gives me a lot of productivity while I am
      coding. For example, Svelte has a lot of features, for example, built-in
      transition, animation, state management, and more. Even Svelte give me
      some useful utilities that help me a lot, for example, scoped style,
      unused CSS code, and more.
   </p>

   <p>
      On the performant side, Svelte is not inferior to other Javascript
      frontend frameworks or even Svelte has better performance in some
      situations. And I'm also doing research on why Rich Harris (Svelte's
      author) makes Svelte, what problem he solves, and more.
   </p>

   <p>Yeahh, that is what I think about this phenomenon.... Thanks</p>
